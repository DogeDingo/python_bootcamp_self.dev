# in ist ein Operator

# Beispiel ob Monika in der Liste ist

students = ["Max", "Monika", "Erik", "Franziska"]

print("Monika" in students)

# Ergebnis: True > sprich JA Monika ist in der Liste

# Sowas funktioniert auch mit Strings

sentence = "Ja, die Monika studiert hier!"

if "!" in sentence:
    print("JA")

